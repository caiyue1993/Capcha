import UIKit

extension UIColor {
    
    class func capchaRed() -> UIColor {
        return UIColor(hex: 0xE83F1A)
    }
    
    class func capchaNavigationBarTintColor() -> UIColor {
        return UIColor(hex: 0x424242)
    }
    
    class func capchaLineColor() -> UIColor {
        return UIColor(hex: 0x979797)
    }
}

